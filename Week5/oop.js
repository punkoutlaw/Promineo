// The 4 pillars of Object-Oriented Programming (OOP):

// 1) Abstraction: is when you show code when you want to show it (to access, it has to be inside a class, it can't access it outside of the class).
// 2) Encapsulation: Removing access to parts of your code and making things private is exactly what Encapsulation is all about.
// 3) Inheritance: Inheritance lets one object acquire the properties and methods of another object.
// 4) Polymorphism: is to override a parent classes method. If there are 2 things (classes) that extend the same class, they can have the same functionality but return something different. They can do the same things in different ways.

