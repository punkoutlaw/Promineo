class Cars {
    make
    model
    color
    year
    bodyType
    mileage

    constructor(make, model, color, year, bodyType, mileage) {
        this.make = make;
        this.model = model;
        this.color = color;
        this.year = year;
        this.bodyType = bodyType
        this.mileage = mileage;
    }
}